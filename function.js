
 /* music functions */
$(function() {
	$("#music").draggable({ containment: 'window' });
 });

 $(document).ready(function(){
	$("#musicClose").click(function(){
	  $("#music").hide();
	});
	$("#musicBtn").click(function(){
	  $("#music").show();
	});
  });

